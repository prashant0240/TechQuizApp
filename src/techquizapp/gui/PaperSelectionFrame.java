    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
        String subject=jcLanguage.getSelectedItem().toString();
        String itom=jcItom.getSelectedItem().toString();
        int totalQuest=Integer.parseInt(itom);
        int ans;
        ans=JOptionPane.showConfirmDialog(null,"You have chosen\""+subject+"\"and \""+totalQuest+"\"questions!\n is this ok?","Conformation!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try{
                String examId=ExamDAO.getExamId();

                ExamPojo newExam=new ExamPojo(examId,subject,totalQuest);

                TakeTestFrame testFrame=new TakeTestFrame(newExam);

                testFrame.setVisible(true);
                this.dispose();
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error while Connecting to DB","Exception",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnTakeTestActionPerformed

